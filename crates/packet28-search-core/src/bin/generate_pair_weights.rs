use std::fs;
use std::path::{Path, PathBuf};

use anyhow::{Context, Result};
use ignore::WalkBuilder;

const GENERATED_VERSION: u32 = 2;

fn main() -> Result<()> {
    let root = std::env::args()
        .nth(1)
        .map(PathBuf::from)
        .unwrap_or(std::env::current_dir()?);
    let output_path = std::env::args()
        .nth(2)
        .map(PathBuf::from)
        .unwrap_or_else(|| {
            PathBuf::from(env!("CARGO_MANIFEST_DIR"))
                .join("src")
                .join("generated_pair_weights.rs")
        });
    let weights = build_pair_weights(&root)?;
    let rendered = render_generated_weights(&weights);
    fs::write(&output_path, rendered).with_context(|| {
        format!(
            "failed to write generated pair weights '{}'",
            output_path.display()
        )
    })?;
    eprintln!(
        "wrote generated pair weights from '{}' to '{}'",
        root.display(),
        output_path.display()
    );
    Ok(())
}

fn build_pair_weights(root: &Path) -> Result<[u16; 65_536]> {
    let mut counts = [0u64; 65_536];
    let mut walker = WalkBuilder::new(root);
    walker
        .hidden(false)
        .git_ignore(true)
        .git_exclude(true)
        .git_global(true);
    for entry in walker.build() {
        let entry = match entry {
            Ok(entry) => entry,
            Err(_) => continue,
        };
        let path = entry.path();
        if path.is_dir() || should_skip_path(root, path) {
            continue;
        }
        let bytes = match fs::read(path) {
            Ok(bytes) => bytes,
            Err(_) => continue,
        };
        if bytes.is_empty() || bytes.contains(&0) {
            continue;
        }
        for pair in bytes.windows(2) {
            let left = pair[0].to_ascii_lowercase();
            let right = pair[1].to_ascii_lowercase();
            counts[((usize::from(left)) << 8) | usize::from(right)] += 1;
        }
    }

    let mut order = (0u32..65_536u32)
        .map(|pair| {
            let idx = pair as usize;
            (counts[idx], pair as u16)
        })
        .collect::<Vec<_>>();
    order.sort_unstable_by(|left, right| left.0.cmp(&right.0).then_with(|| left.1.cmp(&right.1)));

    let mut weights = [0u16; 65_536];
    let total = order.len() as u32;
    for (rank, (_, pair)) in order.into_iter().enumerate() {
        let score = total.saturating_sub(rank as u32 + 1) as u16;
        weights[pair as usize] = score.saturating_add(1);
    }
    Ok(weights)
}

fn should_skip_path(root: &Path, path: &Path) -> bool {
    let Ok(relative) = path.strip_prefix(root) else {
        return true;
    };
    let normalized = relative.to_string_lossy().replace('\\', "/");
    normalized.starts_with(".git/")
        || normalized.starts_with(".packet28/")
        || normalized.starts_with("target/")
        || normalized.starts_with("node_modules/")
}

fn render_generated_weights(weights: &[u16; 65_536]) -> String {
    let mut out = String::new();
    out.push_str("// @generated by cargo run -p packet28-search-core --bin generate_pair_weights -- <root>\n");
    out.push_str(&format!(
        "pub const WEIGHT_TABLE_VERSION: u32 = {GENERATED_VERSION};\n"
    ));
    out.push_str("pub static PAIR_WEIGHTS: [u16; 65_536] = [\n");
    for chunk in weights.chunks(16) {
        out.push_str("    ");
        let mut first = true;
        for weight in chunk {
            if !first {
                out.push_str(", ");
            }
            first = false;
            out.push_str(&weight.to_string());
        }
        out.push_str(",\n");
    }
    out.push_str("];\n");
    out
}
